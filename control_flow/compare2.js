// Compare two numbers, and handle the special case where the two numbers are equal

var n1 = 40, n2 = 50;

// Single equals (=) means assignment
// Double equals (==) means comparison

if (n1 == n2) {
    console.log("n1 and n2 are equal");
}
else if (n1 > n2) {
    console.log("n1 is greater");
}
else {
    console.log("n2 is greater");
}

