// Add more than one number and print the sum
console.log("Sum:", 10 + 20 + 30 + 40);

// Create a variable using var
// temp1 is the name of the variable
// The expression on the right of the = is the value assigned to the variable
var temp1 = 10 + 20 + 30 + 40;

// You can recall the value of a variable by using its name
console.log("temp1 =", temp1);

// The variable's value can be changed... this is why it is called a variable
temp1 = 50;
console.log("temp1 =", temp1);

// You can mix and match
console.log("New sum =", temp1 * 2);
var ten = 10;
console.log("times ten =", temp1 * ten);


// Numbers can be negative
console.log("Negative:", 10 - 20);
